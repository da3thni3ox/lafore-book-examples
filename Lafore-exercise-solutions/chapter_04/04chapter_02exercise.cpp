// Написать программу с использованием структуры, представляющей точку на плоскости,
// задаваемую двумя координатами

#include <io.h> // для функции _setmode
#include <fcntl.h> // для константы _O_U16TEXT
#include <iostream>
using namespace std;

struct point // точка на плоскости
{
	int x;   // координата по горизонтальной оси
	int y;   // координата по вертикальной оси
};

int main()
{
	// переключение стандартного потока вывода в формат Юникода
	_setmode(_fileno(stdout), _O_U16TEXT);
	// переключение стандартного потока ввода в формат Юникода
	_setmode(_fileno(stdin), _O_U16TEXT);

	point p1, p2, p3; // создание трех структурных переменных типа point

	// инициализация структурных переменных p1 и p2
	wcout << L"Введите координаты точки p1: "; wcin >> p1.x >> p1.y;
	wcout << L"Введите координаты точки p2: "; wcin >> p2.x >> p2.y;

	// сложение координат первой и второй точек
	p3.x = p1.x + p2.x;
	p3.y = p1.y + p2.y;

	// вывод результата сложения на экран
	wcout << L"Координаты точки p1+p2 равны: " << p3.x << L" " << p3.y << endl;

	return 0;
}